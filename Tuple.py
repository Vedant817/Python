# These are special types of data types which can't be modified
marks = (55, 65, 10)
print(marks)

# Another way of representation
score = 65, 60, 96
print(score[1])

# Tuples can be used to perform the count operations means a particular element exists how many times in the particular tuple.
print(marks.count(1))
print(marks.index(65))
